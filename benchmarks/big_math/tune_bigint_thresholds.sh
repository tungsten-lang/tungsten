#!/usr/bin/env sh
set -eu

DIR=$(CDPATH= cd -- "$(dirname -- "$0")" && pwd)
ROOT=$(CDPATH= cd -- "$DIR/../.." && pwd)
OUT="$ROOT/runtime/generated/bigint_thresholds.h"
LOG="$ROOT/runtime/generated/bigint_thresholds.last.txt"

REPS=${REPS:-3}
RANGES=${RANGES:-"8:512:8 640:4096:128"}

mkdir -p "$ROOT/runtime/generated"

# shellcheck disable=SC2086
if "$DIR/run_toom_sweep.sh" --ntt --reps "$REPS" $RANGES > "$LOG"; then
  cat "$LOG"
else
  cat "$LOG"
  exit 1
fi

pick_first_best() {
  awk -v want="$1" '
    /^[[:space:]]*[0-9]+[[:space:]]/ && $NF == want { print $1; exit }
  ' "$LOG"
}

kara_first=$(pick_first_best toom2 || true)
toom3_first=$(pick_first_best toom3 || true)
toom4_first=$(pick_first_best toom4 || true)
ntt_first=$(pick_first_best ntt || true)

kara=${kara_first:-49}
if [ "$kara" -gt 1 ]; then
  kara=$((kara - 1))
fi
toom3=${toom3_first:-128}
toom4=${toom4_first:-384}
ntt=${ntt_first:-2048}

{
  echo "/* Auto-generated by benchmarks/big_math/tune_bigint_thresholds.sh."
  echo " * Host: $(uname -sm)"
  echo " * CC: ${CC:-clang}"
  echo " * REPS=$REPS RANGES=\"$RANGES\""
  echo " * Review runtime/generated/bigint_thresholds.last.txt before committing."
  echo " */"
  echo "#ifndef TUNGSTEN_BIGINT_THRESHOLDS_GENERATED_H"
  echo "#define TUNGSTEN_BIGINT_THRESHOLDS_GENERATED_H"
  echo "#define BN_KARA_THRESHOLD $kara"
  echo "#define BN_TOOM3_THRESHOLD $toom3"
  echo "#define BN_TOOM4_THRESHOLD $toom4"
  echo "#define BN_NTT_THRESHOLD $ntt"
  echo "#endif"
} > "$OUT"

printf 'wrote %s\n' "$OUT"
printf 'candidate thresholds: BN_KARA=%s BN_TOOM3=%s BN_TOOM4=%s BN_NTT=%s\n' "$kara" "$toom3" "$toom4" "$ntt"
