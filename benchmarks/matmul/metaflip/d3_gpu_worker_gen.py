"""Generate the bounded 6x6 C3 x Z2 Metal quotient worker.

The campaign executes only the checked-in Tungsten/Metal pair.  This script is
an offline reproducibility aid: it starts from the ordinary C3 worker template
and widens every orbit toggle to the distinct set orbit generated by C3 and
coordinate reversal.  No Python process is used while FlipFleet is running.
"""

from __future__ import annotations

import argparse
from pathlib import Path

from c3_gpu_worker_gen import generate as generate_c3


def _replace_once(text: str, old: str, new: str, label: str) -> str:
    count = text.count(old)
    if count != 1:
        raise ValueError(f"{label}: expected one template occurrence, found {count}")
    return text.replace(old, new, 1)


def _image_block(indent: str, n: int, mask_type: str) -> str:
    bits = n * n
    i = indent
    return f"""{i}tq = 0
{i}trr = 0
{i}ts = 0
{i}rq = 0
{i}rr = 0
{i}rs = 0
{i}rtq = 0
{i}rtrr = 0
{i}rts = 0
{i}b = 0
{i}while b < {bits}
{i}  row = b / {n}
{i}  col = b % {n}
{i}  dst = col * {n} + row
{i}  revdst = ({n - 1} - row) * {n} + ({n - 1} - col)
{i}  revtdst = ({n - 1} - col) * {n} + ({n - 1} - row)
{i}  if ((q >> b) & one) == one
{i}    tq = tq | (one << dst)
{i}    rq = rq | (one << revdst)
{i}    rtq = rtq | (one << revtdst)
{i}  if ((r >> b) & one) == one
{i}    trr = trr | (one << dst)
{i}    rr = rr | (one << revdst)
{i}    rtrr = rtrr | (one << revtdst)
{i}  if ((s >> b) & one) == one
{i}    ts = ts | (one << dst)
{i}    rs = rs | (one << revdst)
{i}    rts = rts | (one << revtdst)
{i}  b = b + 1
{i}x = q
{i}y = r
{i}z = s
{i}if orbit == 1
{i}  x = r
{i}  y = ts
{i}  z = tq
{i}if orbit == 2
{i}  x = ts
{i}  y = q
{i}  z = trr
{i}if orbit == 3
{i}  x = rq
{i}  y = rr
{i}  z = rs
{i}if orbit == 4
{i}  x = rr
{i}  y = rts
{i}  z = rtq
{i}if orbit == 5
{i}  x = rts
{i}  y = rq
{i}  z = rtrr

{i}# Toggle each distinct set-orbit image once.  C3-fixed terms and terms
{i}# whose reversal is already in their C3 orbit have nontrivial stabilizers.
{i}fixed = 0
{i}if q == r
{i}  if r == ts
{i}    if s == tq
{i}      fixed = 1
{i}sameorbit = 0
{i}if rq == q
{i}  if rr == r
{i}    if rs == s
{i}      sameorbit = 1
{i}if rq == r
{i}  if rr == ts
{i}    if rs == tq
{i}      sameorbit = 1
{i}if rq == ts
{i}  if rr == q
{i}    if rs == trr
{i}      sameorbit = 1
{i}emit = 1
{i}if fixed == 1
{i}  if orbit == 1
{i}    emit = 0
{i}  if orbit == 2
{i}    emit = 0
{i}if sameorbit == 1
{i}  if orbit >= 3
{i}    emit = 0
{i}if emit == 0
{i}  x = 0
{i}  y = 0
{i}  z = 0"""


def _old_image_block(indent: str, n: int) -> str:
    bits = n * n
    i = indent
    return f"""{i}tq = 0
{i}trr = 0
{i}ts = 0
{i}b = 0
{i}while b < {bits}
{i}  row = b / {n}
{i}  col = b % {n}
{i}  dst = col * {n} + row
{i}  if ((q >> b) & one) == one
{i}    tq = tq | (one << dst)
{i}  if ((r >> b) & one) == one
{i}    trr = trr | (one << dst)
{i}  if ((s >> b) & one) == one
{i}    ts = ts | (one << dst)
{i}  b = b + 1
{i}x = q
{i}y = r
{i}z = s
{i}if orbit == 1
{i}  x = r
{i}  y = ts
{i}  z = tq
{i}if orbit == 2
{i}  x = ts
{i}  y = q
{i}  z = trr"""


def generate(n: int = 6, cap: int = 240,
             metal_ll: str = "benchmarks/matmul/metaflip/d3_bundle/d3_666.ll") -> str:
    if n != 6:
        raise ValueError("the bounded C3 x Z2 experiment is intentionally 6x6-only")
    text = generate_c3(n, cap, metal_ll)
    text = text.replace("Generated by c3_gpu_worker_gen.py", "Generated by d3_gpu_worker_gen.py")
    text = text.replace("Every mutation below is performed by XOR-toggling all three orbit images.",
                        "Every mutation XOR-toggles the distinct C3 x Z2 set orbit (at most six images).")
    text = text.replace("c3_walk", "d3_walk")
    text = text.replace("C3GPU_", "D3GPU_")
    text = text.replace("C3GPU_RESULT", "D3GPU_RESULT")

    declarations = """    px = 0 ## i64"""
    widened = """    px = 0 ## i64
    rq = 0 ## i64
    rr = 0 ## i64
    rs = 0 ## i64
    rtq = 0 ## i64
    rtrr = 0 ## i64
    rts = 0 ## i64
    revdst = 0 ## i32
    revtdst = 0 ## i32
    fixed = 0 ## i32
    sameorbit = 0 ## i32
    emit = 0 ## i32"""
    text = _replace_once(text, declarations, widened, "GPU orbit declarations")

    text = _replace_once(
        text,
        "while phase < 12\n            which = phase / 3\n            orbit = phase % 3",
        "while phase < 24\n            which = phase / 6\n            orbit = phase % 6",
        "ordinary D3 flip orbit",
    )
    text = _replace_once(
        text,
        "while phase < 9\n                  which = phase / 3\n                  orbit = phase % 3",
        "while phase < 18\n                  which = phase / 6\n                  orbit = phase % 6",
        "D3 plus orbit",
    )
    text = text.replace("if rank <= cap - 6", "if rank <= cap - 12")
    text = text.replace("if startrank > CAP - 6", "if startrank > CAP - 12")
    text = text.replace("if BAND < 0 || BAND > CAP - 6", "if BAND < 0 || BAND > CAP - 12")

    for indent, label in (("            ", "ordinary image block"),
                          ("                  ", "plus image block")):
        text = _replace_once(text, _old_image_block(indent, n),
                             _image_block(indent, n, "i64"), label)

    host_anchor = """-> c3_closed(us, vs, ws, rank) (i64[] i64[] i64[] i64) i64"""
    host_helpers = """-> reverse_mask(v) (i64) i64
  out = 0 ## i64
  b = 0
  while b < 36
    if ((v >> b) & 1) == 1
      row = b / 6
      col = b % 6
      out = out | (1 << ((5 - row) * 6 + (5 - col)))
    b += 1
  out

-> z2_closed(us, vs, ws, rank) (i64[] i64[] i64[] i64) i64
  ok = 1 ## i64
  i = 0
  while i < rank
    x = reverse_mask(us[i])
    y = reverse_mask(vs[i])
    z = reverse_mask(ws[i])
    found = 0
    j = 0
    while j < rank
      if us[j] == x && vs[j] == y && ws[j] == z
        found = 1
      j += 1
    if found == 0
      ok = 0
    i += 1
  ok

-> c3_closed(us, vs, ws, rank) (i64[] i64[] i64[] i64) i64"""
    text = _replace_once(text, host_anchor, host_helpers, "host Z2 gate")

    seed_anchor = """seed_c3 = c3_closed(seedu, seedv, seedw, startrank)
seed_factors = factors_valid(seedu, seedv, seedw, startrank)"""
    seed_widened = """seed_c3 = c3_closed(seedu, seedv, seedw, startrank)
seed_z2 = z2_closed(seedu, seedv, seedw, startrank)
seed_factors = factors_valid(seedu, seedv, seedw, startrank)"""
    text = _replace_once(text, seed_anchor, seed_widened, "seed Z2 value")
    seed_gate = """if seed_c3 == 0
  << "D3GPU_ERROR seed_exact=1 seed_c3=0"
  exit(2)"""
    seed_gate_widened = """if seed_c3 == 0 || seed_z2 == 0
  << "D3GPU_ERROR seed_exact=1 seed_c3=" + seed_c3.to_s() + " seed_z2=" + seed_z2.to_s()
  exit(2)"""
    text = _replace_once(text, seed_gate, seed_gate_widened, "seed D3 gate")

    candidate_anchor = """c3ok = c3_closed(outu, outv, outw, bestrank)
factorok = factors_valid(outu, outv, outw, bestrank)"""
    candidate_widened = """c3ok = c3_closed(outu, outv, outw, bestrank)
z2ok = z2_closed(outu, outv, outw, bestrank)
factorok = factors_valid(outu, outv, outw, bestrank)"""
    text = _replace_once(text, candidate_anchor, candidate_widened, "candidate Z2 value")
    write_gate = """if vok == 1
  if c3ok == 1
    if factorok == 1
      write_file(outpath, body)"""
    write_gate_widened = """if vok == 1
  if c3ok == 1
    if z2ok == 1
      if factorok == 1
        write_file(outpath, body)"""
    text = _replace_once(text, write_gate, write_gate_widened, "candidate D3 gate")
    text = text.replace('" c3_closed=" + c3ok.to_s() + " factors_valid="',
                        '" c3_closed=" + c3ok.to_s() + " z2_closed=" + z2ok.to_s() + " factors_valid="')
    return text


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--output", type=Path,
                        default=Path("benchmarks/matmul/metaflip/d3_bundle/d3_666.w"))
    args = parser.parse_args()
    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(generate(), encoding="utf-8")


if __name__ == "__main__":
    main()


__all__ = ["generate"]
