    if len(sys.argv) > 1 and sys.argv[1] == "check":
        ok = soundness_report()
        print("SOUNDNESS:", "OK" if ok else "FAILED")
        sys.exit(0 if ok else 1)
    group_name = sys.argv[1]
    if group_name not in GROUPS:
        sys.exit(f"unknown group {group_name!r}; expected one of {sorted(GROUPS)}")
    n, recv = int(sys.argv[2]), int(sys.argv[3])
    seed = sys.argv[4] if len(sys.argv) > 4 and sys.argv[4] != "naive" else None
    cap = int(sys.argv[5]) if len(sys.argv) > 5 else 14000000000
    plusper = int(sys.argv[6]) if len(sys.argv) > 6 else 200
